package Oct.ex_21102024_ForLoop;

public class Lab098 {
    public static void main(String[] args) {
        //to find the even number from 1 to 50
        for (int i = 0; i < 50; i++) {
      if(i % 2==0){
          System.out.println("Even");
      }
      else {
          System.out.println("Odd");
      }
        }
    }
}
