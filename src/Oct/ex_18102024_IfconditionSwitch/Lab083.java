package Oct.ex_18102024_IfconditionSwitch;

public class Lab083 {
    public static void main(String[] args) {
        char code ='C';

        switch (code){
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
            default:
                System.out.println("Default");
                break;
        }
    }
}
